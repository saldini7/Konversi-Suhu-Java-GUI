    private void celciusTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_celciusTextKeyReleased
        double celcius = Double.parseDouble(celciusText.getText());
        double fahrenheit =celcius * 1.8 + 32;
        double kelvin = celcius + 273.15;
        double reamur = celcius * 0.8;
        fahrenheitText.setText("" + fahrenheit);
        kelvinText.setText("" + kelvin);
        reamurText.setText("" + reamur); 
    }//GEN-LAST:event_celciusTextKeyReleased

    private void fahrenheitTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fahrenheitTextKeyReleased
        double fahrenheit = Double.parseDouble(fahrenheitText.getText());
        double kelvin =(fahrenheit + 459.67) / 1.8;
        double celcius = (fahrenheit - 32) / 1.8;
        double reamur = (fahrenheit - 32) / 2.25;
        celciusText.setText("" + celcius);
        kelvinText.setText("" + kelvin);
        reamurText.setText("" + reamur); 
    }//GEN-LAST:event_fahrenheitTextKeyReleased

