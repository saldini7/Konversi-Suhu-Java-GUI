    private void reamurTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_reamurTextKeyReleased
        double reamur = Double.parseDouble(reamurText.getText());
        double kelvin = reamur / 0.8 + 273.15;
        double celcius = reamur/ 0.8;
        double fahrenheit = (reamur - 7.5) * 24/7 + 32;
        celciusText.setText("" + celcius);
        kelvinText.setText("" + kelvin);
        fahrenheitText.setText("" + fahrenheit); 
    }//GEN-LAST:event_reamurTextKeyReleased

    private void kelvinTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kelvinTextKeyReleased
        double kelvin = Double.parseDouble(kelvinText.getText());
        double celcius = kelvin - 273.15;
        double fahrenheit = kelvin * 1.8 - 459.67;
        double reamur = (kelvin - 273.15) * 0.8;
        celciusText.setText("" + celcius);
        reamurText.setText("" + reamur);
        fahrenheitText.setText("" + fahrenheit); 
    }//GEN-LAST:event_kelvinTextKeyReleased

